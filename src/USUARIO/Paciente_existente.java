private void id_paciente() {
     int contador_id=0;
     int c=0;
        String[] Registro = new String[1];
        String sql = "SELECT id_paciente FROM db_paciente";
        Statement st;
        try {
            st = cnu.createStatement();
            ResultSet rs = st.executeQuery(sql);
            while (rs.next()) {
                Registro[0] = rs.getString("id_paciente");
                c = Integer.parseInt(Registro[0]);
            }
            for (int i = 0; i <= c; i++) {
                contador_id = i + 1;
            }
        } catch (SQLException ex) {
            Logger.getLogger(Paciente_existente.class.getName()).log(Level.SEVERE, null, ex);
        }
        txt_paciente.setText(""+contador_id);
    }
